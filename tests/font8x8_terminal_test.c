static void dump_char(char ch) {
  const uint8_t *bitmap;
  int row;
  int col;

  if ((ch < 32) || (ch > 127)) {
    fprintf(stderr, "unsupported char: %d\n", (int)ch);
    exit(1);
  }

  bitmap = font8x8[ch - 32];
  printf("char '%c'\n", ch);
  printf("bytes:");
  for (row = 0; row < 8; ++row) {
    printf(" %02X", bitmap[row]);
  }
  printf("\n");

  for (row = 0; row < 8; ++row) {
    for (col = 0; col < 8; ++col) {
      putchar((bitmap[row] & (uint8_t)(0x80u >> col)) ? '#' : '.');
    }
    putchar('\n');
  }
}

int main(int argc, char **argv) {
  int index;

  if (argc == 1) {
    dump_char('T');
    dump_char('X');
    return 0;
  }

  for (index = 1; index < argc; ++index) {
    dump_char(argv[index][0]);
  }

  return 0;
}
